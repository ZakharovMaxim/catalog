import {formatDate} from '@/tools/moment'
describe('moment.js', () => {
  it('formate date correctly', () => {
    let date = new Date(2018, 0, 1, 20)
    expect(formatDate(date)).to.equal('01 янв')
    date = new Date(2018, 1, 2, 20)
    expect(formatDate(date)).to.equal('02 фев')
    date = new Date(2018, 2, 3, 20)
    expect(formatDate(date)).to.equal('03 мар')
    date = new Date(2018, 3, 4, 20)
    expect(formatDate(date)).to.equal('04 апр')
    date = new Date(2018, 4, 5, 20)
    expect(formatDate(date)).to.equal('05 май')
    date = new Date(2018, 5, 6, 20)
    expect(formatDate(date)).to.equal('06 июн')
    date = new Date(2018, 6, 7, 20)
    expect(formatDate(date)).to.equal('07 июл')
    date = new Date(2018, 7, 8, 20)
    expect(formatDate(date)).to.equal('08 авг')
    date = new Date(2018, 8, 9, 20)
    expect(formatDate(date)).to.equal('09 сен')
    date = new Date(2018, 9, 10, 20)
    expect(formatDate(date)).to.equal('10 окт')
    date = new Date(2018, 10, 11, 20)
    expect(formatDate(date)).to.equal('11 ноя')
    date = new Date(2018, 11, 12, 20)
    expect(formatDate(date)).to.equal('12 дек')
  })
})
